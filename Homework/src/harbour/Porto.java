package harbour;

public class Porto {
	//attributes
	private String nome;
	private int imbarcazioni;
	private double metriQuadri;
	
	//methods
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getImbarcazioni() {
		return imbarcazioni;
	}
	public void setImbarcazioni(int imbarcazioni) {
		this.imbarcazioni = imbarcazioni;
	}
	public double getMetriQuadri() {
		return metriQuadri;
	}
	public void setMetriQuadri(double metriQuadri) {
		this.metriQuadri = metriQuadri;
	}
	
}
