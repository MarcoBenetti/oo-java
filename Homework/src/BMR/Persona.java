package BMR;

public class Persona {
	double altezza;
	double peso;
	int eta;
	char sesso;
	String attivitaFisica;
	
	public Persona(double altezza, double peso, int eta, char sesso, String attivitaFisica){
		this.altezza = altezza;
		this.peso = peso;
		this.eta = eta;
		this.sesso = sesso;
		this.attivitaFisica = attivitaFisica;
	}
}
